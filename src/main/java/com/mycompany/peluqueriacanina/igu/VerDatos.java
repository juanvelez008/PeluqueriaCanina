    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // controlar que la tabla no este vacia
        if(tablaMascotas.getRowCount() > 0){
            //controlar que se haya seleccionado al menos un dato
            if(tablaMascotas.getSelectedRow() != -1){
                //obtengo ID de la mascota a editar
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));
                
                ModificarDatos pantallaModif = new ModificarDatos(num_cliente);
                pantallaModif.setVisible(true);
                pantallaModif.setLocationRelativeTo(null);
                this.dispose();
                }
            else {
                mostrarMensaje("No selecciono ninguna mascota", "Error", "Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No existen valores para eliminar en la tabla", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


    public void cargarTabla() {
        // definir el modelo de la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //bloquea filas y columnas para que no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
            return false;
            }
        };
        //Establecemos los nombres de las columnas
        String titulos[] = {"Num","Nombre","Color","Raza","Alergico","At. esp.","Nombre due√±o","Celular"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la DB
        List<Mascota> listaMascotas = control.traerMascotas();
        
        //recorrer la lista y mostrar cada uno de los elementos en la tabla
        if(listaMascotas != null){
            for (Mascota masco : listaMascotas){
                Object[]objeto = {masco.getNum_cliente(),masco.getNombre_perro(),masco.getColor(),masco.getRaza(),
                    masco.getAlergico(),masco.getEspecial(),masco.getUnDuenio().getNombre(),masco.getUnDuenio().getCelular()};
                
                modeloTabla.addRow(objeto);
            }
        }
    
        tablaMascotas.setModel(modeloTabla);
    }

    private void cargarDatos(int num_cliente) {
        this.masco = control.traerMascota(num_cliente);
        txtNombre.setText(masco.getNombre_perro());
        txtRaza.setText(masco.getRaza());
        txtColor.setText(masco.getColor());
        txtNomDuenio.setText(masco.getUnDuenio().getNombre());
        txtCelDuenio.setText(masco.getUnDuenio().getCelular());
        txtObservaciones.setText(masco.getObservaciones());
        
        if(masco.getAlergico().equals("Si")){
            cmbAlergico.setSelectedIndex(1);
        } else if(masco.getAlergico().equals("No")){
            cmbAlergico.setSelectedIndex(2);
        }
        
        if(masco.getEspecial().equals("Si")){
            cmbEspecial.setSelectedIndex(1);
        } else if(masco.getEspecial().equals("No")){
            cmbEspecial.setSelectedIndex(2);
        }
        //}
    }
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if (tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
